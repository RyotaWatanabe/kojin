	{
		for (int j = 0; j < MAP_WIDTH_MAX; j++)
		{
			switch (mapTable[i][j])
			{
				//通れるところ
			case 0:
				break;
				//ブロック描画
			case 1:
				SpriteData blockData;
				blockData.pos = D3DXVECTOR3(j * CHIP_SIZE + g_stageScrollPos.x, i * CHIP_SIZE + g_stageScrollPos.y, 0);
				block->Draw(&blockData);
				break;
			}
		}
	}
	return S_OK;
